export function visualKindForFile(file) {
  const normalized = String(file)
    .replace(/\\/g, '/')
    .replace(/^\.\//, '')
  const knowledgeBaseIndex = normalized.indexOf('10-knowledge-base/')
  const key = knowledgeBaseIndex >= 0 ? normalized.slice(knowledgeBaseIndex) : normalized
  const kind = PAGE_DIAGRAM_KIND[key]
  if (!kind) {
    throw new Error(`Missing visual taxonomy assignment for ${key}`)
  }
  return kind
}
